lista = []
pares = []
impares = []
for i in range(10):
  lista.append(int(input('Digite um número: ')))
  if lista[i] % 2 == 0:
    pares.append(lista[i])
  else:
    impares.append(lista[i])
print('Lista dos números digitados:', lista)
print('Lista dos números pares:', pares)
print('Lista dos números impares:', impares)
