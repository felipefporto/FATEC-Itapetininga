#Dada a lista L= [5, 7, 2, 9, 4, 1, 3],escreva um programa que imprima as seguintes informações:
#a)tamanho da lista.
#b)maior valor da lista.
#c)menor valor da lista.
#d)soma de todos os elementos da lista.
L= [5, 7, 2, 9, 4, 1, 3]
print(L)
print("Tamanho da lista: ", len(L))
print("Menor número da lista: ", min(L))
print("Maior número da lista: ", max(L))
print("A soma da lista: ", sum(L))
