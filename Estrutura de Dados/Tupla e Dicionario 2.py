estados = {'São Paulo': 'SP', 
           'Paraná': 'PR', 
           'Minas Gerais': 'MG'}
sudeste = ['São Paulo','Minas Gerais']
for estado in estados.keys():
  print(estado)
  if estado in sudeste:
    print(estado, 'é localizado na região Sudeste')
  else:
    print(estado, 'não é localizado na região Sudeste')
print('-------------')
estados2 = estados.copy()
estados2['Santa Catarina'] = 'SC'
print(estados)
print(estados2)
print('-------------')
for estado in sorted(estados2.keys()):
  print(estado)
  if estado in sudeste:
    print(estado, 'é localizado na região Sudeste')
  else:
    print(estado, 'não é localizado na região Sudeste')
print('-------------')
